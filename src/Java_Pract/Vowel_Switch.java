package Java_Pract;

import java.util.Scanner;

public class Vowel_Switch {
	
	

	public static void main(String[] args) {
    	
		char input='u';
    	
    	switch(input) {
    		case 'a':
    			break;
    		
    		case 'e':
    			break;
        		
    		case 'i':
    			break;
        		
    		case 'o':
    			break;
        		
    		case 'u':
        		System.out.println(input + " is a Vowel");
        		break;
        	default:
        		System.out.println(input + " is a consonant");
        		
    	}

   
    }

}

